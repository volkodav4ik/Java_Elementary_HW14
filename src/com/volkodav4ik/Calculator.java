package com.volkodav4ik;

public class Calculator {

    public int sum(int a, int b) {
        return a + b;
    }

    public int subtraction(int a, int b) {
        return a - b;
    }

    public double multiplication(int a, int b) {
        return (double) a * b;
    }

    public double division(int a, int b) {
        return (double) a / b;
    }
}
